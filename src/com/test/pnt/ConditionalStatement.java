package com.test.pnt;

public class ConditionalStatement {

	public static void main(String[] args) {
		String city = "Abidjan";
//		System.out.println("the city is  "+city);

		if (city == "Bouake") {
			System.out.println(" the city is Bouake");

		} else if (city == "SP") {
			System.out.println(" the city is SP");

		} else if (city == "Man") {
			System.out.println(" the city is Man");

		} else if (city == "KGO") {
			System.out.println(" the city is KGO"); 
			
		} else {
			System.out.println("Unknown city");
		}	

	}

}
