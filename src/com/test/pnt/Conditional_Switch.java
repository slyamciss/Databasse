package com.test.pnt;

public class Conditional_Switch {

	public static void main(String[] args) {
		int Month = 2;

		switch (Month) {
		case 1:
			System.out.println("The mnth is january");
			break;
		case 2:
			System.out.println("The mnth is February");
			break;
		case 3:
			System.out.println("The mnth is March");
			break;
		case 4:
			System.out.println("The mnth is Avril");
			break;
		case 5:
			System.out.println("The mnth is Mai");
			break;
		case 6:
			System.out.println("The mnth is jun");
			break;
		case 7:
			System.out.println("The mnth is july");
			break;
		case 8:
			System.out.println("The mnth is August");
			break;

		default:
			System.out.println("Unknown month");
		
		}

	}

}
